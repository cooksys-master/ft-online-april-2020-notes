package com.cooksys.collections.hotel;

public class Guest {
	
	private String name;
	
	public Guest(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return name;
	}

}
